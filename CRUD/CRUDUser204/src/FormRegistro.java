    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Guardar en variables lo que escribe el usuarip
        String nom= txtNombre.getText();
        String contra= txtContrasena.getText();
        String corr= txtCorreo.getText();
        
        //Validar que no existan datos vacios
        if(nom.isEmpty() || contra.isEmpty() || corr.isEmpty()){
        JOptionPane.showMessageDialog(this,"Todos los campos son obligatorios","Error",JOptionPane.INFORMATION_MESSAGE);
        return;
        }
        
        //3. Procedemos  a insertar
        UserCRUD crud= new UserCRUD();
        boolean status= crud.InsertUsuario(nom, contra, corr);
        
        //4. Avisamos status
        if(status){
         JOptionPane.showMessageDialog(this,"Usuario Guardado","Exito",JOptionPane.INFORMATION_MESSAGE);
        }
        else{
        JOptionPane.showMessageDialog(this,"Usuario no Guardado","Error",JOptionPane.INFORMATION_MESSAGE);
        }
        limpiarcampos();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void limpiarcampos(){
        txtNombre.setText("");
        txtContrasena.setText("");
        txtCorreo.setText("");
    }
