    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
       //1. Obtener el valor del txt
       String idtext= txtId.getText();
       
       //2. Validar vacio
        if(idtext.isEmpty()){
        JOptionPane.showMessageDialog(this,"Todos los campos son obligatorios","Error",JOptionPane.ERROR_MESSAGE);
        return;
        }
        
        //3. Busqueda y llenado de la tabla
        try{
            //Se prepara el id para que sea un entero
            int id= Integer.parseInt(idtext);
            ResultSet rs= crud.BuscarPorID(id);
            DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel(); //Inicializa el modelo e iguala el componente de la tabla
            modelo.setRowCount(0);
            
            if(rs.next()){
                modelo.addRow(new Object[]{rs.getInt("id"),rs.getString("nombre"),rs.getString("contrasena"),rs.getString("correo")});
            }
                
        }
        catch(SQLException e){
        System.out.println("Error al llenar la tabla" + e.getMessage());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnTodosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTodosActionPerformed
        ResultSet todos= crud.obtenerTodos();
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0);
        
        try{
            while(todos.next()){
                modelo.addRow(new Object[]{todos.getInt("id"),todos.getString("nombre"),todos.getString("contrasena"),todos.getString("correo")});
            }
        }
        catch(SQLException x){
            System.out.println("Error al llenar la tabla" + x.getMessage());
        }
    }//GEN-LAST:event_btnTodosActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        int filaSeleccionada = jTable1.getSelectedRow();
    
        // Obtener el ID de la fila seleccionada (primera columna)
        int id = (int) jTable1.getValueAt(filaSeleccionada, 0);
        String nombre = (String) jTable1.getValueAt(filaSeleccionada, 1);
        String contrasena = (String) jTable1.getValueAt(filaSeleccionada, 2);
        String correo = (String) jTable1.getValueAt(filaSeleccionada, 3);
    
        FormActualizar FA = new FormActualizar(id, nombre, contrasena, correo);
        FA.setVisible(true);
    }//GEN-LAST:event_btnActualizarActionPerformed

